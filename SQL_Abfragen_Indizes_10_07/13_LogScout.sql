/*
	SQL LogScout ist ein Tool von Microsoft, das zur Diagnose und Analyse von
	SQL Server-Problemen entwickelt wurde. 

	- Hilft dabei Protokolle und Diagnosedaten von SQL Server-Instanzen zu sammeln
		=> Gut für Datenbankadministratoren & Support-Teams

	Wichtige Funktionen von SQL LogScout:
	1. Automatisierte Datensammlung: 
	- Sammelt automatisch Protokolle, Fehlermeldungen ohne das der Administrator überprüfen muss

	2. Leistungsanalyse: 
	- Erfasst Leistungsdaten, um Engpässe oder Leistungsprobleme des SQL Servers zu identifiziern

	3. Flexible Protokollerstellung:
	- gezielte Analyse durch bestimmte Konfiguration von Protokollen oder Diagnosedaten

	4. Einfacher Export:
	- gesammelte Daten lassen sich leicht an den Microsoft-Support oder andere Analyse-Tools weiterleiten

	5. Reduzierte Fehleranalysezeit:
	- Durch die Automatisierung der Datensammlung kann SQL LogScout die Zeit für Problemidentifikation
	  reduzieren


	Anwendungsfälle:
	- SQL Server Instanzen werden langsamer
	- Fehlermeldungen angezeigt werden
	- Verbindungsprobleme 
	=> praktische Lösung zur schnellen und effizienten Fehlerdiagnose
https://github.com/microsoft/SQL_LogScout
*/