/*
	SQLNexus ist ein Diagnosetool, das verwendet wird, um Leistungsprobleme in
	SQL Server-Umgebungen zu analysieren und zu beheben.
	=> Importiert und verarbeitet Daten, die mithilfe andere Tools wie SQL Server Profiler
	   gesammelt wurden und erstellt dann visuelle Berichte zur Problemursache


	Hauptfunktion von SQLNexus:
	1. Datenimport aus verschiedenen Quellen wie z.B SQL Server Profiler

	2. Analyse und Berichterstellung:
	 - generiert detaillierte Berichte, die Leistungsprobleme aufzeigen
	   z. B Engpässe bei CPU,Speicher oder Festplatten und gibt Einblicke in die SQL
	   Server-Abfrageleistung

	3. Visualisierungen:
	 - Stellt Daten visuell dar, die Berichte umfassen Diagramme und Tabellen, die 
	   die Serverleistung und potenzielle Probleme aufzeigen

	4. Automatisierte Leistungsanalyse:
	 - SQLNexus wertet die gesammelten Daten automatisch aus und hebt die problematischsten
	   Abfragen, Sitzungen und Ressourcen hervor

	5. Kostenlos und anpassbar: SQLNexus ist kostenlos und ermöglicht die Anpassung der Berichte
	   und Analysen an spezifische Anforderungen


	Einsatzgebiete von SQLNexus:
	- Eignet sich zur Untersuchung von Leistungsproblemen in produktiven SQL Server-Umgebungen
	- SQL Server-Administratoren verwenden es häufig zur Optimierung von Datenbankabfragen und -operationen
	  durch analysieren von ineffiziente Abfragen, Locking und hohe CPU- oder Speicherlast
*/
https://github.com/microsoft/SqlNexus